console.log('Різниця: ' + operand1 + '-' + operand2 + ' = ' + sub);
const mult = operand1 * operand2;
console.log('Добуток: ' + operand1 + '*' + operand2 + ' = ' + mult);
const div = operand1 / operand2;
console.log('Частка: ' + operand1 + '/' + operand2 + ' = ' + div);  